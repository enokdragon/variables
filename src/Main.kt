fun main() {
    var myByte: Byte = 7
    var myShort: Short = 22
    var myInt: Int = 6
    var myLong: Long = 7171717
    var myFloat: Float = 7.7731F
    var myDouble: Double = 171.0928
    var myBool: Boolean = true
    var myChar = 'g'

    print("Byte demo: $myByte")
    print("\nShort demo: $myShort")
    print("\nInteger demo: $myInt")
    print("\nLong demo: $myLong")
    print("\nFloat demo: $myFloat")
    print("\nDouble demo: $myDouble")
    print("\nBoolean demo: $myBool")
    print("\nCharacter demo:$myChar")
}